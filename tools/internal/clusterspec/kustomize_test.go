// allOn is the default component set (every component enabled except DefaultDisabled).
func allOn() map[string]ComponentToggle {
	m := map[string]ComponentToggle{}
	for _, c := range Components {
		m[c.Name] = ComponentToggle{Enabled: boolPtr(!c.DefaultDisabled)}
	}
	return m
}

func TestRenderManifestKustomization(t *testing.T) {
	out := RenderManifestKustomization(allOn(), "", "", "")
	// Thin overlay: the shared base + a health-inert Application CR under resources:
	// for each enabled CARVED component (blast-radius decomposition) + a components:
	// list of the remaining plain component dirs.
	for _, want := range []string{
		"GENERATED by", "kind: Kustomization",
		"- ../../../../platform-apl/manifest",
		// carved components → App CRs under resources:, NOT components: dirs
		"- llz-externalsecrets.yaml",
		"- llz-observability.yaml",
		"- llz-harbor.yaml",
		"- llz-reconciler.yaml",
		// plain (non-carved) components stay as components: dirs
		"- ../../../../platform-apl/components/openbao",
		"- ../../../../platform-apl/components/certManager",
	} {
		if !strings.Contains(out, want) {
			t.Errorf("thin overlay missing %q:\n%s", want, out)
		}
	}
	// Carved components no longer appear as components: dirs, and their per-env
	// patches move into their own App source root — so the manifest overlay carries
	// NO patches: section (every patch-bearing component is carved).
	for _, absent := range []string{
		"- ../../../../platform-apl/components/externalSecrets",
		"- ../../../../platform-apl/components/observability",
		"- ../../../../platform-apl/components/harbor",
		"- ../../../../platform-apl/components/llzReconciler",
		"patches:", "llz-reconciler-env-patch.yaml",
	} {
		if strings.Contains(out, absent) {
			t.Errorf("carved component leaked %q into the manifest overlay:\n%s", absent, out)
		}
	}
	// Mandatory components (argocd, clusterFoundation) live in the base — never in
	// the components: list.
	if strings.Contains(out, "components/argocd") || strings.Contains(out, "components/clusterFoundation") {
		t.Errorf("mandatory components must not be listed (they're in the base):\n%s", out)
	}
	// Disabling harbor drops its carved App CR (and only it).
	off := allOn()
	off["harbor"] = ComponentToggle{Enabled: boolPtr(false)}
	dropped := RenderManifestKustomization(off, "", "", "")
	if strings.Contains(dropped, "llz-harbor.yaml") {
		t.Error("disabled harbor should drop its carved App CR")
	}
	if !strings.Contains(dropped, "llz-observability.yaml") {
		t.Error("disabling harbor must not drop sibling carved Apps")
	}
}

func TestRenderManifestKustomization_RemoteRefs(t *testing.T) {
	const ref = "v9.9.9"
	out := RenderManifestKustomization(allOn(), ref, "", "")
	// Token-free plain components are fetched from the template repo at the pinned ref.
	for _, want := range []string{
		"- github.com/akamai-consulting/lke-landing-zone//platform-apl/components/openbao?ref=v9.9.9&timeout=80",
		"- github.com/akamai-consulting/lke-landing-zone//platform-apl/components/certManager?ref=v9.9.9&timeout=80",
	} {
		if !strings.Contains(out, want) {
			t.Errorf("remote ref missing %q:\n%s", want, out)
		}
	}
	// The git-fetch timeout override (kustomize's 27s default is too short for the
	// ~35MB repo) must ride every remote ref.
	if strings.Contains(out, "?ref=v9.9.9\n") || !strings.Contains(out, "&timeout=80") {
		t.Errorf("every remote ref must carry &timeout=80:\n%s", out)
	}
	// The shared platform-apl/manifest base is fetched remotely too (Phase 1); its per-
	// instance instance-custom ApplicationSet is split OUT and stays a local resource.
	if !strings.Contains(out, "- github.com/akamai-consulting/lke-landing-zone//platform-apl/manifest?ref=v9.9.9") {
		t.Errorf("shared base should be a remote ref:\n%s", out)
	}
	if !strings.Contains(out, "- instance-custom.yaml") {
		t.Errorf("instance-custom ApplicationSet must stay a local resource:\n%s", out)
	}
	// An empty ref keeps everything local (drift-check default / no instance context).
	local := RenderManifestKustomization(allOn(), "", "", "")
	if strings.Contains(local, "github.com/akamai-consulting") {
		t.Errorf("empty ref must stay fully local:\n%s", local)
	}
	if !strings.Contains(local, "- ../../../../platform-apl/manifest") || !strings.Contains(local, "- ../../../../platform-apl/components/openbao") {
		t.Errorf("empty ref should reference the base + components locally:\n%s", local)
	}
	// spec.dns.acmeEmail set → an inline ClusterIssuer patch carries the contact
	// (the shared dns tree ships email: "" and is now remote, so it can't be rewritten).
	withEmail := RenderManifestKustomization(allOn(), ref, "ops@example.com", "")
	for _, want := range []string{"kind: ClusterIssuer", "path: /spec/acme/email", "value: ops@example.com"} {
		if !strings.Contains(withEmail, want) {
			t.Errorf("acmeEmail patch missing %q:\n%s", want, withEmail)
		}
	}
	if strings.Contains(out, "/spec/acme/email") {
		t.Errorf("unset acmeEmail must emit no ClusterIssuer patch:\n%s", out)
	}
}

func TestRenderCarvedAppKustomization_RemoteRefs(t *testing.T) {
	const ref = "v9.9.9"
	// A token-free carved component (observability) is fetched remotely at the ref.
	obs, _ := LookupComponent("observability")
	if got := RenderCarvedAppKustomization(obs, ref, ""); !strings.Contains(got,
		"- github.com/akamai-consulting/lke-landing-zone//platform-apl/components/observability?ref=v9.9.9") {
		t.Errorf("observability carved kustomization should reference the remote ref:\n%s", got)
	}
	// llzReconciler is remote (Phase 0 moved its image to an images: transformer), and
	// with an imageTag set the carved kustomization retags the shared :latest image.
	rec, _ := LookupComponent("llzReconciler")
	got := RenderCarvedAppKustomization(rec, ref, "sha-abc123")
	if !strings.Contains(got, "components/llzReconciler?ref=v9.9.9") {
		t.Errorf("llzReconciler should be a remote ref now:\n%s", got)
	}
	for _, want := range []string{"images:", "name: ghcr.io/akamai-consulting/llz", "newTag: sha-abc123"} {
		if !strings.Contains(got, want) {
			t.Errorf("llzReconciler carved kustomization missing image retag %q:\n%s", want, got)
		}
	}
	// A component that doesn't run the llz image (observability) gets no images: block.
	if strings.Contains(RenderCarvedAppKustomization(obs, ref, "sha-abc123"), "images:") {
		t.Errorf("observability should not get an llz images: transformer")
	}
}

// clusterHealthWorkflow is a PLAIN (non-carved) component fetched remotely from
// platform-apl; its Argo WorkflowTemplate image (a CRD the images: transformer can't
// reach) is retagged by a JSON6902 patch in the manifest overlay when it's enabled
// and an image tag is resolved.
func TestRenderManifestKustomization_HealthWorkflowRetag(t *testing.T) {
	comps := allOn()
	comps["clusterHealthWorkflow"] = ComponentToggle{Enabled: boolPtr(true)}
	out := RenderManifestKustomization(comps, "v9.9.9", "", "sha-abc123")
	for _, want := range []string{
		"- github.com/akamai-consulting/lke-landing-zone//platform-apl/components/clusterHealthWorkflow?ref=v9.9.9",
		"kind: WorkflowTemplate",
		"name: llz-cluster-health",
		"path: /spec/templates/0/container/image",
		"value: ghcr.io/akamai-consulting/llz:sha-abc123",
	} {
		if !strings.Contains(out, want) {
			t.Errorf("health retag missing %q:\n%s", want, out)
		}
	}
	// No image tag → no retag patch (the component still ships :latest remotely).
	if noTag := RenderManifestKustomization(comps, "v9.9.9", "", ""); strings.Contains(noTag, "/spec/templates/0/container/image") {
		t.Errorf("no imageTag must emit no WorkflowTemplate retag:\n%s", noTag)
	}
	// Disabled → no retag even with an image tag.
	off := allOn()
	off["clusterHealthWorkflow"] = ComponentToggle{Enabled: boolPtr(false)}
	if strings.Contains(RenderManifestKustomization(off, "v9.9.9", "", "sha-abc123"), "WorkflowTemplate") {
		t.Error("disabled clusterHealthWorkflow must emit no retag patch")
	}
}

func TestRenderInstanceCustom(t *testing.T) {
	app := RenderInstanceCustom("https://github.com/acme/inst.git")
	for _, want := range []string{
		"kind: ApplicationSet",
		"name: instance-custom",
		"project: instance-custom",
		"repoURL: https://github.com/acme/inst.git",
		`argocd.argoproj.io/sync-wave: "10"`,
		"prune: false", // operator content is theirs to delete deliberately
		"GENERATED by",
		// App Platform's GitOps layout: one App per namespaces/<ns>/ dir + one global.
		"- path: kubernetes-custom/namespaces/*",
		"- path: kubernetes-custom/global",
		"name: instance-custom-{{.path.basename}}",
		// global/ → default namespace; namespaces/<ns>/ → <ns>. Chosen in the single
		// top-level template so no generator carries a CRD-invalid partial template.
		`namespace: '{{ if eq .path.basename "global" }}default{{ else }}{{ .path.basename }}{{ end }}'`,
		"goTemplate: true",
		"CreateNamespace=true",
		// Removing a dir from git must orphan resources, never cascade-delete them.
		"preserveResourcesOnDeletion: true",
	} {
		if !strings.Contains(app, want) {
			t.Errorf("instance-custom ApplicationSet missing %q:\n%s", want, app)
		}
	}
	// DATA-LOSS GUARD. Removing a directory from git must orphan its resources, never
	// tear down running workloads. Verified in argo-cd source: preserveResourcesOnDeletion
	// keeps resources-finalizer OFF generated Apps (it is implemented in the RENDER path),
	// and Argo only cascades to cluster resources when that finalizer is present. Both
	// assertions below are load-bearing, and each guards a distinct way to silently arm
	// destruction:
	//   1. dropping/flipping the flag — the finalizer is then re-applied to EXISTING apps
	//      on the next reconcile, with no git change at all;
	//   2. declaring ANY finalizer in the template — the render only adds the finalizer
	//      when the template has none, so a template finalizer voids the flag entirely.
	// Note prune:false does NOT buy this; it governs sync-time diffing, not the cascade.
	if !strings.Contains(app, "preserveResourcesOnDeletion: true") {
		t.Errorf("preserveResourcesOnDeletion is the ONLY thing preventing cascade-delete on directory removal:\n%s", app)
	}
	if strings.Contains(app, "finalizers:") || strings.Contains(app, "resources-finalizer.argocd.argoproj.io") {
		t.Errorf("no finalizer may appear: any template finalizer voids preserveResourcesOnDeletion:\n%s", app)
	}
	// CRD-VALIDITY GUARD. There must be exactly ONE template — the top-level one. A
	// per-generator `template:` block that omits metadata/spec.project (as an earlier
	// revision had, to repoint only the global generator's namespace) is rejected by
	// the ApplicationSet CRD's admission validation ("spec.generators[N].git.template.
	// spec.project: Required value"), so the ENTIRE ApplicationSet never applies while
	// platform-bootstrap stays Healthy — the silent break assert-instance-custom
	// exists to catch. Keep generators template-free; choose the destination namespace
	// in the top-level template (goTemplate) instead.
	if n := strings.Count(app, "template:"); n != 1 {
		t.Errorf("expected exactly 1 (top-level) template, found %d — a per-generator template block fails ApplicationSet CRD admission validation:\n%s", n, app)
	}
	// The hatch DELIBERATELY floats on the default branch — "drop a file, Argo applies
	// it" must hold even on a release-pinned instance, so it does NOT track
	// apps_repo_revision the way platform-bootstrap and the carved Apps do.
	// Both generators AND the App template must float in lockstep: discovering
	// directories at one revision while syncing their contents at another would be a
	// genuinely confusing failure.
	if strings.Count(app, "revision: HEAD") != 2 || !strings.Contains(app, "targetRevision: HEAD") {
		t.Errorf("both generators + the App template must float on HEAD:\n%s", app)
	}
	// Guard the regression directly: no rendered revision may leak in from the spec.
	for _, pin := range []string{"v1.2.3", "main", "targetRevision: v", "revision: v"} {
		if strings.Contains(app, pin) {
			t.Errorf("the hatch must not carry a pinned revision (%q):\n%s", pin, app)
		}
	}
	// The repo is the ONLY per-instance value — a different repo flows through verbatim.
	if !strings.Contains(RenderInstanceCustom("https://github.com/other/repo.git"), "repoURL: https://github.com/other/repo.git") {
		t.Error("repoURL should be substituted verbatim")
	}
}

func TestRenderCarvedApp(t *testing.T) {
	es, _ := LookupComponent("externalSecrets")
	app := RenderCarvedApp(es, "prod", "https://github.com/acme/inst.git", "v1.2.3")
	for _, want := range []string{
		"kind: Application",
		"name: llz-externalsecrets",
		"project: platform-bootstrap",
		`argocd.argoproj.io/sync-wave: "-10"`, // externalSecrets is the dependency root → lowest
		"repoURL: https://github.com/acme/inst.git",
		"targetRevision: v1.2.3",
		"path: apl-values/prod/apps/externalSecrets",
		"namespace: external-secrets",
		"SkipDryRunOnMissingResource=true",
	} {
		if !strings.Contains(app, want) {
			t.Errorf("carved App CR missing %q:\n%s", want, app)
		}
	}
	// externalSecrets (dependency root) must carry a strictly lower App wave than
	// every consumer App — the ordering the wave-dependency-guard enforces.
	esWave := es.CarvedApp.AppWave
	for _, name := range []string{"observability", "harbor", "llzReconciler"} {
		c, _ := LookupComponent(name)
		if c.CarvedApp == nil {
			t.Fatalf("%s should be carved", name)
		}
		if esWave >= c.CarvedApp.AppWave {
			t.Errorf("externalSecrets App wave %d must be < %s App wave %d", esWave, name, c.CarvedApp.AppWave)
		}
	}
}

