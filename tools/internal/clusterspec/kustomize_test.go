package clusterspec

import (
	"strings"
	"testing"
)

// allOn is the default component set (every component enabled except DefaultDisabled).
func allOn() map[string]ComponentToggle {
	m := map[string]ComponentToggle{}
	for _, c := range Components {
		m[c.Name] = ComponentToggle{Enabled: boolPtr(!c.DefaultDisabled)}
	}
	return m
}

func TestRenderManifestKustomization(t *testing.T) {
	out := RenderManifestKustomization(allOn())
	// Thin overlay: the shared base + a components: list of enabled toggleable
	// component dirs + the per-env volume-labeler patch.
	for _, want := range []string{
		"GENERATED by", "kind: Kustomization",
		"- ../../_shared/manifest",
		"- ../../components/externalSecrets",
		"- ../../components/harbor",
		"patches:", "path: linode-volume-labeler-region-patch.yaml", "kind: CronJob",
	} {
		if !strings.Contains(out, want) {
			t.Errorf("thin overlay missing %q:\n%s", want, out)
		}
	}
	// Mandatory components (argocd, clusterFoundation) live in the base — never in
	// the components: list.
	if strings.Contains(out, "components/argocd") || strings.Contains(out, "components/clusterFoundation") {
		t.Errorf("mandatory components must not be listed (they're in the base):\n%s", out)
	}
	// Disabling harbor drops its component dir (and only it).
	off := allOn()
	off["harbor"] = ComponentToggle{Enabled: boolPtr(false)}
	if strings.Contains(RenderManifestKustomization(off), "../../components/harbor") {
		t.Error("disabled harbor should drop its component dir")
	}
}

func TestSetACMEEmail(t *testing.T) {
	// Two ClusterIssuers (production + staging), each with an acme email line.
	base := `spec:
  acme:
    email: REPLACE_PER_ENV   # e.g. ops@example.com
---
spec:
  acme:
    email: REPLACE_PER_ENV   # e.g. ops@example.com
`
	got := SetACMEEmail(base, "ops@example.com")
	if strings.Contains(got, "REPLACE_PER_ENV") {
		t.Errorf("placeholder survived:\n%s", got)
	}
	if n := strings.Count(got, "email: ops@example.com"); n != 2 {
		t.Errorf("both issuer emails should be set, got %d:\n%s", n, got)
	}
	// Idempotent: re-rendering the already-filled content is a no-op.
	if again := SetACMEEmail(got, "ops@example.com"); again != got {
		t.Errorf("SetACMEEmail not idempotent:\n%s", again)
	}
	// Change-safe: a new email rewrites the existing value (not just the placeholder).
	changed := SetACMEEmail(got, "sre@example.com")
	if strings.Count(changed, "email: sre@example.com") != 2 || strings.Contains(changed, "ops@example.com") {
		t.Errorf("email change not applied:\n%s", changed)
	}
}

func TestComponentRegistry_AplCoreMapping(t *testing.T) {
	// The unified model routes some components to the apl-core (values.yaml) backend.
	obs, ok := LookupComponent("observability")
	if !ok {
		t.Fatal("observability component missing")
	}
	for _, app := range []string{"prometheus", "loki", "grafana"} {
		found := false
		for _, a := range obs.AplCoreApps {
			if a == app {
				found = true
			}
		}
		if !found {
			t.Errorf("observability should map to apl-core app %q (got %v)", app, obs.AplCoreApps)
		}
	}
	// harbor spans BOTH backends.
	h, _ := LookupComponent("harbor")
	if len(h.AplCoreApps) == 0 || len(h.ManifestResources) == 0 {
		t.Errorf("harbor should use both backends; got aplCore=%v manifest=%v", h.AplCoreApps, h.ManifestResources)
	}
	// ComponentEnabled returns false for an unknown component and honors a partial map.
	if ComponentEnabled(map[string]ComponentToggle{}, "nonexistent") {
		t.Error("an unknown component → not enabled")
	}
	if !ComponentEnabled(map[string]ComponentToggle{}, "openbao") {
		t.Error("openbao defaults enabled")
	}
}
