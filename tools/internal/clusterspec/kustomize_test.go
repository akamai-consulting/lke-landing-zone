func TestComponentRegistry_AplCoreMapping(t *testing.T) {
	// The unified model routes some components to the apl-core (values.yaml) backend.
	obs, ok := LookupComponent("observability")
	if !ok {
		t.Fatal("observability component missing")
	}
	for _, app := range []string{"prometheus", "loki", "grafana"} {
		found := false
		for _, a := range obs.AplCoreApps {
			if a == app {
				found = true
			}
		}
		if !found {
			t.Errorf("observability should map to apl-core app %q (got %v)", app, obs.AplCoreApps)
		}
	}
	// harbor spans BOTH backends.
	h, _ := LookupComponent("harbor")
	if len(h.AplCoreApps) == 0 || len(h.ManifestResources) == 0 {
		t.Errorf("harbor should use both backends; got aplCore=%v manifest=%v", h.AplCoreApps, h.ManifestResources)
	}
	// ComponentEnabled returns false for an unknown component and honors a partial map.
	if ComponentEnabled(map[string]ComponentToggle{}, "nonexistent") {
		t.Error("an unknown component → not enabled")
	}
	if !ComponentEnabled(map[string]ComponentToggle{}, "openbao") {
		t.Error("openbao defaults enabled")
	}
}
