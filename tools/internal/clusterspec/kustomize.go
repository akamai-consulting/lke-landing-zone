package clusterspec

import "strings"

// kustomize.go renders the two committed-but-derived kustomizations under
// apl-values/<env>/manifest/ from a deployment's enabled recipes:
//
//   - kustomization.yaml        — the parent tree apl-operator's Argo CD syncs;
//     its resources: list selects which raw manifests + the argocd/ subdir apply.
//   - argocd/kustomization.yaml — the AppProject + the wave-ordered component
//     Applications.
//
// These are the ONLY files the spec commits (the rest of the per-env overlay is
// cloned from example and the tfvars are transient): in-cluster Argo CD syncs
// committed git, so the recipe selection cannot be a working-tree-only render.
// They carry a generated header and are verified against the spec by
// `llz render --check`. Output is deterministic (registry order) so the check is
// stable and diffs stay localized.

const genHeader = `# GENERATED by ` + "`llz render`" + ` from llz.yaml — DO NOT EDIT.
# Toggle components via spec.environments.<env>.recipes in llz.yaml, then re-run
# ` + "`llz render`" + `. See docs/landing-zone-spec.md.
`

const kustomizePreamble = genHeader + `apiVersion: kustomize.config.k8s.io/v1beta1
kind: Kustomization
`

// RenderManifestKustomization returns the parent manifest kustomization.yaml for
// the given recipe toggles: the resources: contributed by each enabled recipe
// (in registry order) plus any patches they bring.
func RenderManifestKustomization(recipes map[string]RecipeToggle) string {
	var resources []string
	var patches []Patch
	for _, r := range Recipes {
		if !RecipeEnabled(recipes, r.Name) {
			continue
		}
		resources = append(resources, r.ManifestResources...)
		patches = append(patches, r.Patches...)
	}

	var b strings.Builder
	b.WriteString(kustomizePreamble)
	b.WriteString("\nresources:\n")
	for _, res := range resources {
		b.WriteString("  - " + res + "\n")
	}
	if len(patches) > 0 {
		b.WriteString("\npatches:\n")
		for _, p := range patches {
			b.WriteString("  - path: " + p.Path + "\n")
			b.WriteString("    target:\n")
			b.WriteString("      group: " + p.Group + "\n")
			b.WriteString("      version: " + p.Version + "\n")
			b.WriteString("      kind: " + p.Kind + "\n")
			b.WriteString("      name: " + p.Name + "\n")
		}
	}
	return b.String()
}

// RenderArgoKustomization returns argocd/kustomization.yaml: the wave-ordered
// AppProject + component Applications contributed by each enabled recipe.
func RenderArgoKustomization(recipes map[string]RecipeToggle) string {
	var resources []string
	for _, r := range Recipes {
		if !RecipeEnabled(recipes, r.Name) {
			continue
		}
		resources = append(resources, r.ArgoApps...)
	}

	var b strings.Builder
	b.WriteString(kustomizePreamble)
	b.WriteString("\nresources:\n")
	for _, res := range resources {
		b.WriteString("  - " + res + "\n")
	}
	return b.String()
}
